x = 10
y = 4

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x // y)  # Floor division: rounds down to the nearest whole number
print(x ** y)  # Exponentiation: x raised to the power of y
print(x % y)  # Modulus


